#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	size_t	s_len;
	size_t	i;

	s_len = ft_strlen(s1);
	dest = (char *)malloc(sizeof(char) * s_len + 1);
	if (dest == NULL)
		return (0);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
